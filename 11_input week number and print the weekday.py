a=int(input("Enter week number:"))
if a==1:
    print("1st day is Monday")
elif a==2:
    print("2nd day is Tuesday")
elif a==3:
    print("3rd day is Wednesday")
elif a==4:
    print("4th day is Thursday")
elif a==5:
    print("5th day is Friday")
elif a==6:
    print("6th day is Saturday")
elif a==7:
    print("7th day is Sunday")
else:
    print("Invalid week number.")



